def add(p1,p2):
    print(f" {p1} + {p2} = {p1+p2}")

def sub(p1,p2):
    print(f" {p1} - {p2} = {p1-p2}")

def mul(p1, p2):
    print(f" {p1} * {p2} = {p1 * p2}")

def div(p1, p2):
    print(f" {p1} / {p2} = {p1 / p2}")

if(__name__ == '__main__'):
    add(4,5)
    sub(4,2)
    mul(5,4)
